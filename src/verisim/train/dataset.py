"""Build supervised training examples from oracle rollouts (SPEC-2 §5.3 Stage 1).

Each example is ``(prompt_ids, target_ids)`` where the prompt is ``<bos> state
action <gen>`` and the target is the encoded delta + ``<eos>`` (SPEC-2 §5.2).
Examples are generated by rolling a seeded driver against the oracle, so the
training set is a deterministic function of (config, driver, seed) -- the same
reproducibility regime as the trajectory datasets (SPEC-2 §12).
"""

from __future__ import annotations

import random

import torch
from torch import Tensor

from verisim.data.drivers import Driver
from verisim.env.config import EnvConfig
from verisim.env.state import State
from verisim.oracle.base import Oracle

from ..model.tokenizer import encode_prompt, encode_state_target, encode_target
from ..model.vocab import Vocab

IGNORE_INDEX = -100
Example = tuple[list[int], list[int]]


def examples_from_rollout(
    oracle: Oracle,
    vocab: Vocab,
    config: EnvConfig,
    driver: str,
    seed: int,
    n_steps: int,
    max_depth: int | None = None,
) -> list[Example]:
    """``(prompt_ids, target_ids)`` for each step of one seeded rollout."""
    driver_obj = Driver(name=driver, config=config, rng=random.Random(seed), max_depth=max_depth)
    state = State.empty()
    examples: list[Example] = []
    for _ in range(n_steps):
        action = driver_obj.sample(state)
        result = oracle.step(state, action)
        examples.append(
            (
                encode_prompt(state, action, vocab),
                encode_target(result.delta, result.stdout, vocab),
            )
        )
        state = result.state
    return examples


def state_examples_from_rollout(
    oracle: Oracle,
    vocab: Vocab,
    config: EnvConfig,
    driver: str,
    seed: int,
    n_steps: int,
    max_depth: int | None = None,
) -> list[Example]:
    """Like :func:`examples_from_rollout` but with the *full next state* as target.

    The full-state representation (SPEC-2 §9 ablation): the prompt is the same
    ``<bos> state action <gen>``, but the target is :func:`encode_state_target`
    (the whole next state + ``<eos>``) instead of the encoded delta.
    """
    driver_obj = Driver(name=driver, config=config, rng=random.Random(seed), max_depth=max_depth)
    state = State.empty()
    examples: list[Example] = []
    for _ in range(n_steps):
        action = driver_obj.sample(state)
        result = oracle.step(state, action)
        examples.append(
            (
                encode_prompt(state, action, vocab),
                encode_state_target(result.state, result.stdout, vocab),
            )
        )
        state = result.state
    return examples


def build_dataset(
    oracle: Oracle,
    vocab: Vocab,
    config: EnvConfig,
    *,
    driver: str = "weighted",
    seeds: tuple[int, ...] = (0,),
    n_steps: int = 40,
    target: str = "delta",
    max_depth: int | None = None,
) -> list[Example]:
    """Build supervised examples; ``target`` selects the prediction representation.

    ``"delta"`` (default) targets the structured edit sequence (the primary M_θ);
    ``"state"`` targets the full next state (the §9 representation-ablation arm).
    ``max_depth`` is the K3 difficulty dial passed through to the driver (SPEC-2.1 §7).
    """
    builder = examples_from_rollout if target == "delta" else state_examples_from_rollout
    examples: list[Example] = []
    for seed in seeds:
        examples += builder(oracle, vocab, config, driver, seed, n_steps, max_depth)
    return examples


def collate(examples: list[Example], pad_id: int) -> tuple[Tensor, Tensor]:
    """Pad a batch into ``(input_ids, labels)`` for next-token training.

    For each example the full sequence is ``prompt + target``; ``input_ids`` is the
    sequence without its last token and ``labels`` is the sequence shifted by one,
    with prompt positions and padding set to :data:`IGNORE_INDEX` so the loss is
    computed only over the predicted target tokens.
    """
    inputs: list[list[int]] = []
    labels: list[list[int]] = []
    for prompt, target in examples:
        full = prompt + target
        inp = full[:-1]
        lab = full[1:]
        # Predictions before the last prompt token are over the prompt -> ignore.
        for j in range(len(prompt) - 1):
            lab[j] = IGNORE_INDEX
        inputs.append(inp)
        labels.append(lab)

    width = max(len(seq) for seq in inputs)
    inp_t = torch.full((len(inputs), width), pad_id, dtype=torch.long)
    lab_t = torch.full((len(labels), width), IGNORE_INDEX, dtype=torch.long)
    for i, (inp, lab) in enumerate(zip(inputs, labels, strict=True)):
        inp_t[i, : len(inp)] = torch.tensor(inp, dtype=torch.long)
        lab_t[i, : len(lab)] = torch.tensor(lab, dtype=torch.long)
    return inp_t, lab_t
