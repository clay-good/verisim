"""Build supervised training examples from distributed oracle rollouts (SPEC-7 §6.1, DS4).

Each example is ``(prompt_ids, target_ids)`` where the prompt is ``<bos> state action <gen>`` and
the target is the encoded log/replica delta + ``<eos>``. Examples are generated by rolling a seeded
:class:`~verisim.distdata.drivers.DistDriver` against the reference oracle, so the training set is a
deterministic function of ``(config, driver, seed)`` -- the same reproducibility regime as the v0
datasets (SPEC-2 §12), the network trajectories (NW2), and the distributed trajectories (DS2).

The example shape and padding are identical to v0's, so the *generic*, vocab-agnostic trainers in
:mod:`verisim.train.supervised` (``train_supervised`` / ``train_batched`` /
``teacher_forced_accuracy``) and :func:`verisim.train.dataset.collate` are reused unchanged.
"""

from __future__ import annotations

import random

from verisim.dist.config import DistConfig
from verisim.dist.state import DistributedState
from verisim.distdata.drivers import DistDriver
from verisim.distoracle.base import DistOracle
from verisim.train.dataset import Example

from .tokenizer import encode_prompt, encode_target
from .vocab import DistVocab


def dist_examples_from_rollout(
    oracle: DistOracle,
    vocab: DistVocab,
    config: DistConfig,
    driver: str,
    seed: int,
    n_steps: int,
) -> list[Example]:
    """``(prompt_ids, target_ids)`` for each step of one seeded distributed rollout."""
    driver_obj = DistDriver(name=driver, config=config, rng=random.Random(seed))
    state = DistributedState.initial(config)
    examples: list[Example] = []
    for _ in range(n_steps):
        action = driver_obj.sample(state)
        result = oracle.step(state, action)
        examples.append(
            (encode_prompt(state, action, vocab), encode_target(result.delta, vocab))
        )
        state = result.state
    return examples


def build_dist_dataset(
    oracle: DistOracle,
    vocab: DistVocab,
    config: DistConfig,
    *,
    driver: str = "uniform",
    seeds: tuple[int, ...] = (0,),
    n_steps: int = 40,
) -> list[Example]:
    """Build supervised examples across ``seeds`` (the DS4 supervised dataset)."""
    examples: list[Example] = []
    for seed in seeds:
        examples += dist_examples_from_rollout(oracle, vocab, config, driver, seed, n_steps)
    return examples
