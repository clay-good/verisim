"""Experiment CX5: the SCM cross-world fork onto the SYSTEM oracles (H64, SPEC-17 §6).

CX0 proved the *reference* oracle is an exact Structural Causal Model: abduction (reset+replay) is
bit-exact on every world, so rung-3 counterfactuals are exact and free (H60). The natural objection
is that this is a property of the *abstraction*, not of reality — a real shell or a real cluster is
nondeterministic, so the SCM contract might be an artifact of the reference interpreter. CX5 answers
it by re-running the abduction gate on the **system oracles** — the real `/bin/sh` + coreutils
[`SandboxOracle`](../../src/verisim/oracle/sandbox.py) (filesystem world) and the Tier-B
deterministic-simulation [`SystemDistOracle`](../../src/verisim/distoracle/system.py) (distributed
world, autonomous actors under a seeded scheduler). This is the **H64** "the method is a cross-world
recipe" claim at its hardest case: does the SCM framing *survive the move to the system oracle*?

For each system-oracle world, with the action sequence generated by the reference and **replayed
through the system oracle**, CX5 measures: (a) **abduction-exactness** — two independent replays
reproduce the system oracle's trajectory bit-for-bit (the SCM-contract check on the real system);
(b) **counterfactual-exactness** — a rung-3 counterfactual (`do(a')` at step `t`, future held fixed)
is *itself* bit-reproducible across replays; and (c) **cf-differs** — that counterfactual genuinely
differs from the factual, so abduction is doing real work. The reference-oracle rate (1.0, from CX0)
is the anchor: CX5 asks whether the system oracle matches it.

The honest, load-bearing reading: the system oracles are exact SCMs **because** the filesystem
oracle is sealed against clock/RNG/concurrency (the SY4 `DeterminismSeal`) and the distributed
oracle drives its real concurrency with a **seeded** scheduler (the DST thesis, SPEC-7). A real
system *without* that seal/seed is *not* an SCM — so CX5 measures that the seal/seed is exactly what
buys exact, free rung-3 counterfactuals on the real system, not an assumption. Pure-oracle, CPU,
deterministic, seeded; a system oracle genuinely unavailable (no `/bin/sh`, no thread support) is a
**disclosed skip** (SPEC-11 §2.5), never counted as a pass.
"""

from __future__ import annotations

import argparse
import json
from collections.abc import Callable
from dataclasses import dataclass
from pathlib import Path
from statistics import fmean
from typing import Any

from verisim.causal.scm import (
    Intervention,
    abduct_and_replay,
    abduction_exact,
    rung3_counterfactual,
)
from verisim.experiments.cx_common import CXWorld, _dist_cx, _fs_cx


@dataclass(frozen=True)
class CX5Config:
    """A small, fast system-oracle SCM-fork instance (the real `/bin/sh` arm is the slow one)."""

    n_steps: int = 40
    n_seeds: int = 8
    base_seed: int = 0
    intervene_at: float = 0.5  # rung-3 intervention at this fraction of the rollout depth

    @staticmethod
    def from_dict(d: dict[str, Any]) -> CX5Config:
        b = CX5Config()
        return CX5Config(
            n_steps=d.get("n_steps", b.n_steps),
            n_seeds=d.get("n_seeds", b.n_seeds),
            base_seed=d.get("base_seed", b.base_seed),
            intervene_at=d.get("intervene_at", b.intervene_at),
        )

    @staticmethod
    def from_json_file(path: str | Path) -> CX5Config:
        return CX5Config.from_dict(json.loads(Path(path).read_text()))


@dataclass(frozen=True)
class CX5Stat:
    """One system-oracle world's transfer cell: reference anchor vs system-oracle SCM rates."""

    world: str
    available: bool  # False => the system oracle could not run here (disclosed skip)
    ref_abduction: float  # reference-oracle abduction-exactness (the CX0 anchor, expect 1.0)
    sys_abduction: float  # system-oracle abduction-exactness (the H64 question)
    sys_cf_exact: float  # the rung-3 counterfactual is itself bit-reproducible on the system oracle
    sys_cf_differs: float  # ...and genuinely differs from the factual (abduction does real work)
    n: int

    def csv_row(self) -> str:
        return (
            f"{self.world},{int(self.available)},{self.ref_abduction:.6f},"
            f"{self.sys_abduction:.6f},{self.sys_cf_exact:.6f},{self.sys_cf_differs:.6f},{self.n}"
        )


CSV_HEADER = (
    "world,available,ref_abduction,sys_abduction,sys_cf_exact,sys_cf_differs,n"
)


def _fs_system_step() -> Callable[[Any, Any], Any]:
    """The filesystem system oracle: real `/bin/sh` + coreutils. Raises if no shell is available."""
    from verisim.oracle.sandbox import SandboxOracle

    sandbox = SandboxOracle()
    return lambda s, a: sandbox.step(s, a).state


def _dist_system_step() -> Callable[[Any, Any], Any]:
    """The distributed Tier-B system oracle: autonomous actors under a seeded scheduler."""
    from verisim.distoracle.system import SystemDistOracle

    oracle = SystemDistOracle()
    return lambda s, a: oracle.step(s, a).state


# The two worlds with a runnable system oracle (network/host have no committed system oracle).
SYSTEM_WORLDS: tuple[tuple[str, Callable[[], CXWorld[Any, Any]], Callable[[], Any]], ...] = (
    ("filesystem", _fs_cx, _fs_system_step),
    ("distributed", _dist_cx, _dist_system_step),
)


def _world_stat(
    name: str, cx: CXWorld[Any, Any], sys_step: Callable[[Any, Any], Any], config: CX5Config
) -> CX5Stat:
    """Abduction + rung-3 exactness on the system oracle, with the reference rate as the anchor."""
    t = max(0, int(config.intervene_at * config.n_steps))
    ref_ex: list[float] = []
    sys_ex: list[float] = []
    cf_exact: list[float] = []
    cf_differs: list[float] = []
    for s in range(config.n_seeds):
        seed = config.base_seed + s
        ref_ok = abduction_exact(
            cx.make_actions, cx.oracle_step, cx.diverge, seed, config.n_steps
        )
        sys_ok = abduction_exact(cx.make_actions, sys_step, cx.diverge, seed, config.n_steps)
        ref_ex.append(1.0 if ref_ok else 0.0)
        sys_ex.append(1.0 if sys_ok else 0.0)
        # Rung-3 on the SYSTEM oracle: reproducible across replays, and non-trivial vs factual.
        s0, actions, states = abduct_and_replay(cx.make_actions, sys_step, seed, config.n_steps)
        alt = cx.alt_action(states[t], 9000 + seed)
        cf1 = rung3_counterfactual(sys_step, s0, actions, Intervention(t, alt))
        cf2 = rung3_counterfactual(sys_step, s0, actions, Intervention(t, alt))
        same = len(cf1) == len(cf2) and all(
            cx.diverge(a, b) == 0.0 for a, b in zip(cf1, cf2, strict=True)
        )
        cf_exact.append(1.0 if same else 0.0)
        cf_differs.append(1.0 if cx.diverge(states[-1], cf1[-1]) > 0 else 0.0)
    return CX5Stat(
        name, True, fmean(ref_ex), fmean(sys_ex), fmean(cf_exact), fmean(cf_differs), config.n_seeds
    )


def run_cx5(config: CX5Config | None = None) -> list[CX5Stat]:
    """Per system-oracle world: does the SCM contract (CX0/H60) survive to the system oracle?"""
    config = config or CX5Config()
    stats: list[CX5Stat] = []
    for name, cx_factory, sys_factory in SYSTEM_WORLDS:
        try:
            sys_step = sys_factory()
        except Exception:  # SystemOracleUnavailable / SystemDistOracleUnavailable — disclosed skip
            stats.append(CX5Stat(name, False, 1.0, 0.0, 0.0, 0.0, 0))
            continue
        stats.append(_world_stat(name, cx_factory(), sys_step, config))
    return stats


def _print_summary(stats: list[CX5Stat]) -> None:
    print("CX5 / H64 - the SCM framing survives to the system oracle (abduction + rung-3 exact):")
    hdr = f"  {'world':>12} {'avail':>6} {'ref-abd':>8} {'sys-abd':>8} {'cf-exact':>9}"
    print(hdr + f" {'cf-diff':>8}")
    for s in stats:
        if not s.available:
            print(f"  {s.world:>12} {'SKIP':>6} {'—':>8} {'—':>8} {'—':>9} {'—':>8} (unavailable)")
            continue
        print(
            f"  {s.world:>12} {'yes':>6} {s.ref_abduction:>8.3f} {s.sys_abduction:>8.3f} "
            f"{s.sys_cf_exact:>9.3f} {s.sys_cf_differs:>8.3f}"
        )
    ran = [s for s in stats if s.available]
    if not ran:
        print("  verdict: all system oracles unavailable here — re-run on a host with shell.")
        return
    transfer = all(s.sys_abduction >= 0.999 and s.sys_cf_exact >= 0.999 for s in ran)
    nontrivial = all(s.sys_cf_differs > 0 for s in ran)
    verdict = (
        "H64 TRANSFER: abduction and rung-3 counterfactuals are bit-exact on the system oracles "
        f"({', '.join(s.world for s in ran)}) just as on the reference (rate 1.0), and the "
        "counterfactuals genuinely differ from the factual — so exact, free rung-3 cfs "
        "survive the move to the real `/bin/sh` and the Tier-B seeded scheduler. The SY4 seal and "
        "the seeded scheduler (DST) are what make the real system an exact SCM — measured, not "
        "assumed."
        if transfer and nontrivial
        else "system-oracle abduction is NOT uniformly bit-exact — a boundary on which real "
        "systems admit an exact rung 3 (see the per-world rows)."
    )
    print(f"  verdict: {verdict}")


def _plot(stats: list[CX5Stat], path: Path) -> None:  # pragma: no cover - plotting
    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    ran = [s for s in stats if s.available]
    skipped = [s for s in stats if not s.available]
    fig, ax = plt.subplots(figsize=(8.2, 4.6))
    metrics = [
        ("ref abduction (CX0 anchor)", "ref_abduction", "#9bd"),
        ("system abduction", "sys_abduction", "#1f77b4"),
        ("system rung-3 cf-exact", "sys_cf_exact", "#2ca02c"),
        ("system rung-3 cf-differs", "sys_cf_differs", "#ff7f0e"),
    ]
    worlds = [s.world for s in ran]
    x = range(len(worlds))
    n = len(metrics)
    width = 0.8 / n
    for i, (label, attr, color) in enumerate(metrics):
        offs = (i - (n - 1) / 2) * width
        ax.bar([j + offs for j in x], [getattr(s, attr) for s in ran], width,
               color=color, label=label)
    ax.axhline(1.0, color="#888", ls=":", lw=1)
    ax.set_xticks(list(x))
    ax.set_xticklabels([f"{w}\n(system oracle)" for w in worlds], fontsize=9)
    ax.set_ylabel("rate")
    ax.set_ylim(0, 1.12)
    title = "CX5 / H64: the SCM framing survives to the system oracle (real /bin/sh, Tier-B DST)"
    if skipped:
        title += f"\n[skipped (unavailable): {', '.join(s.world for s in skipped)}]"
    ax.set_title(title, fontsize=10)
    ax.legend(fontsize=8, loc="lower center", ncol=2)
    fig.tight_layout()
    path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(path, dpi=110)


def main() -> None:  # pragma: no cover - CLI entry point
    parser = argparse.ArgumentParser(description="CX5 the SCM system-oracle fork (H64).")
    parser.add_argument("--config", type=str, default=None)
    parser.add_argument("--out", type=str, default="figures/cx5_system_oracle.csv")
    parser.add_argument("--plot", type=str, default=None)
    args = parser.parse_args()
    cfg = CX5Config.from_json_file(args.config) if args.config else CX5Config()
    stats = run_cx5(cfg)
    _print_summary(stats)
    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join([CSV_HEADER, *(s.csv_row() for s in stats)]) + "\n")
    print(f"wrote {out}")
    _plot(stats, Path(args.plot) if args.plot else out.with_suffix(".png"))


if __name__ == "__main__":  # pragma: no cover
    main()
