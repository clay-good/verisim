"""Build supervised training examples from network oracle rollouts (SPEC-5 §6.1, NW4).

Each example is ``(prompt_ids, target_ids)`` where the prompt is ``<bos> state action
<gen>`` and the target is the encoded graph delta + ``<eos>``. Examples are generated by
rolling a seeded :class:`~verisim.netdata.NetDriver` against the oracle, so the training set
is a deterministic function of ``(config, driver, seed)`` -- the same reproducibility regime
as the v0 datasets (SPEC-2 §12) and the network trajectories (NW2).

The example shape and padding are identical to v0's, so the *generic*, vocab-agnostic
trainers in :mod:`verisim.train.supervised` (``train_supervised`` / ``train_batched`` /
``teacher_forced_accuracy``) and :func:`verisim.train.dataset.collate` are reused unchanged.
"""

from __future__ import annotations

import random

from verisim.net.config import NetConfig
from verisim.net.state import NetworkState
from verisim.netdata.drivers import NetDriver
from verisim.netoracle.base import NetOracle
from verisim.train.dataset import Example

from .tokenizer import encode_prompt, encode_target
from .vocab import NetVocab


def net_examples_from_rollout(
    oracle: NetOracle,
    vocab: NetVocab,
    config: NetConfig,
    driver: str,
    seed: int,
    n_steps: int,
) -> list[Example]:
    """``(prompt_ids, target_ids)`` for each step of one seeded network rollout."""
    driver_obj = NetDriver(name=driver, config=config, rng=random.Random(seed))
    state = NetworkState.initial(config.hosts)
    examples: list[Example] = []
    for _ in range(n_steps):
        action = driver_obj.sample(state)
        result = oracle.step(state, action)
        examples.append(
            (encode_prompt(state, action, vocab), encode_target(result.delta, vocab))
        )
        state = result.state
    return examples


def build_net_dataset(
    oracle: NetOracle,
    vocab: NetVocab,
    config: NetConfig,
    *,
    driver: str = "weighted",
    seeds: tuple[int, ...] = (0,),
    n_steps: int = 40,
) -> list[Example]:
    """Build supervised examples across ``seeds`` (the NW4 supervised dataset)."""
    examples: list[Example] = []
    for seed in seeds:
        examples += net_examples_from_rollout(oracle, vocab, config, driver, seed, n_steps)
    return examples
