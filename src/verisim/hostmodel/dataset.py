"""Build supervised training examples from host oracle rollouts (SPEC-6 §6.1, HC4).

Each example is ``(prompt_ids, target_ids)`` where the prompt is ``<bos> state action <gen>`` and
the target is the encoded bundle delta + ``<eos>``. Examples are generated by rolling a seeded
:class:`~verisim.hostdata.HostDriver` against the oracle from the boot state, so the training set is
a deterministic function of ``(config, driver, seed)`` -- the same reproducibility regime as the v0
datasets (SPEC-2 §12), the network trajectories (NW2), and the HC2 host trajectories.

The example shape and padding are identical to v0's, so the *generic*, vocab-agnostic trainers in
:mod:`verisim.train.supervised` (``train_supervised`` / ``train_batched`` /
``teacher_forced_accuracy``) and :func:`verisim.train.dataset.collate` are reused unchanged.
"""

from __future__ import annotations

import random

from verisim.host.config import HostConfig
from verisim.host.state import HostState
from verisim.hostdata.drivers import HostDriver
from verisim.hostoracle.base import HostOracle
from verisim.train.dataset import Example

from .tokenizer import encode_prompt, encode_target
from .vocab import HostVocab


def host_examples_from_rollout(
    oracle: HostOracle,
    vocab: HostVocab,
    config: HostConfig,
    driver: str,
    seed: int,
    n_steps: int,
) -> list[Example]:
    """``(prompt_ids, target_ids)`` for each step of one seeded host rollout."""
    driver_obj = HostDriver(name=driver, config=config, rng=random.Random(seed))
    state = HostState.initial()
    examples: list[Example] = []
    for _ in range(n_steps):
        action = driver_obj.sample(state)
        result = oracle.step(state, action)
        examples.append(
            (encode_prompt(state, action, vocab), encode_target(result.delta, vocab))
        )
        state = result.state
    return examples


def build_host_dataset(
    oracle: HostOracle,
    vocab: HostVocab,
    config: HostConfig,
    *,
    driver: str = "forky",
    seeds: tuple[int, ...] = (0,),
    n_steps: int = 40,
) -> list[Example]:
    """Build supervised examples across ``seeds`` (the HC4 supervised dataset)."""
    examples: list[Example] = []
    for seed in seeds:
        examples += host_examples_from_rollout(oracle, vocab, config, driver, seed, n_steps)
    return examples
